#ifndef H89_TYPES_H_
#define H89_TYPES_H_

/// \cond
#include <inttypes.h>
/// \endcond

///
/// \typedef WORD
///
/// 16-bit unsigned integer
///
typedef uint16_t WORD;

///
/// \typedef SWORD
///
/// 16-bit signed integer
///
typedef int16_t SWORD;

///
/// \typedef BYTE
///
/// 8-bit unsigned integer
///
typedef uint8_t BYTE;

///
/// \typedef SBYTE
///
/// 8-bit signed integer
///
typedef int8_t SBYTE;


#endif // H89_TYPES_H_
